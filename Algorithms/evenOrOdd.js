// Create a function that takes an integer as an argument and returns "Even" for even numbers and "Odd" for odd numbers.
// Example
// (even_or_odd(8)) -> Even 
// (even_or_odd(7)) -> Odd 

function even_or_odd (int){
    if(int % 2 === 0){
        return "Even"
    }else{
        return "False";
    }
}